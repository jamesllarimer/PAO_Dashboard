    ADD CONSTRAINT FK_EVENT_ON_EVENT_TYPE FOREIGN KEY (event_type_id) REFERENCES event_type (id);

ALTER TABLE event
    ADD CONSTRAINT FK_EVENT_ON_USER FOREIGN KEY (user_id) REFERENCES user_profile (id);

ALTER TABLE theme_example
    ADD CONSTRAINT FK_THEME_EXAMPLE_ON_THEME FOREIGN KEY (theme_id) REFERENCES theme (id);

ALTER TABLE user_profile
    ADD CONSTRAINT FK_USER_PROFILE_ON_RANK FOREIGN KEY (rank_id) REFERENCES rank (id);

ALTER TABLE user_profile
    ADD CONSTRAINT FK_USER_PROFILE_ON_UNIT FOREIGN KEY (unit_id) REFERENCES unit (id);